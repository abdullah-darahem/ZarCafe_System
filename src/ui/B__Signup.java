    private void btn_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signupActionPerformed
        // Read user input from text fields
        String name = txt_username.getText().trim();
        String address = txt_address.getText().trim();
        String phone = txt_phone.getText().trim();
        String pass = new String(txt_password.getPassword()).trim();
        String confirm = new String(txt_confirm_pass.getPassword()).trim();

        // Check if any field is empty
        if (name.isEmpty() || address.isEmpty() || phone.isEmpty() || pass.isEmpty() || confirm.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields ");
            return; // Stop execution
        }

        // Check if password and confirm password match
        if (!pass.equals(confirm)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match ️");
            return; // Stop execution
        }

        try {
            // Get database connection
            Connection con = DBConnection.getConnection();

            // Check if the phone number already exists
            String checkSql = "SELECT * FROM Customers WHERE PHONE_NUMBER = ?";
            PreparedStatement checkPst = con.prepareStatement(checkSql);
            checkPst.setString(1, phone);
            ResultSet rs = checkPst.executeQuery();

            // If phone number exists, show message
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Phone number already exists! Please Login");
            } else {
                // Insert new customer data into the database
String insertSql = "INSERT INTO Customers (USER_NAME, PHONE_NUMBER, PASSWORD, CUSTOMER_ADDRESS, ROLE) VALUES (?, ?, ?, ?, 'User')";                PreparedStatement pst = con.prepareStatement(insertSql);

                // Set customer values
                pst.setString(1, name);
                pst.setString(2, phone);
                pst.setString(3, pass);
                pst.setString(4, address);

                // Execute insert
                pst.executeUpdate();

                // Show success message
                JOptionPane.showMessageDialog(this, "Account Created Successfully! Please Login");

                // Clear all input fields
                txt_username.setText("");
                txt_address.setText("");
                txt_phone.setText("");
                txt_password.setText("");
                txt_confirm_pass.setText("");

                // Close signup window and open login window
                this.dispose();
                new B__Login().setVisible(true);
            }

            // Close database connection
            con.close();

        } catch (Exception e) {
            // Show error message if something goes wrong
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_signupActionPerformed

