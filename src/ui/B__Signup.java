    private void txt_phoneKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_phoneKeyTyped
        // Get the character that was typed by the user
        char c = evt.getKeyChar();

        // If the character is not a digit (like letters or symbols)
        if (!Character.isDigit(c)) {
            // Prevent the character from being added to the field
            evt.consume(); 
        }

        // If the phone number field already has 11 characters or more
        if (txt_phone.getText().length() >= 11) {
            // Prevent additional characters from being added
            evt.consume();
        }     
    }//GEN-LAST:event_txt_phoneKeyTyped

